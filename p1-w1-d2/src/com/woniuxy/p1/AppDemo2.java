package com.woniuxy.p1;

public class AppDemo2 {
    public static void main(String[] args) {
        int m = 5, n = 3;
        m+=n;
        System.out.println(m); //m=m+n
        m-=n;
        System.out.println(m); //m=m-n
        m*=n;
        System.out.println(m); //m=m*n
        m/=n;
        System.out.println(m); //m=m/n
        m%=n;
        System.out.println(m); //m=m%n
    }
}
