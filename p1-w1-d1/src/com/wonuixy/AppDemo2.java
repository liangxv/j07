package com.wonuixy;

public class AppDemo2 {
    public static void main(String[] args) {
        //整数四则运算
        int m = 10;
        int n = 6;

        System.out.println(m);
        System.out.println(n);
        System.out.println("m+n="+(m + n));
        System.out.println("m-n"+(m - n));
        System.out.println("m*n="+m * n);
        System.out.println("m/n="+m / n);
    }
}
