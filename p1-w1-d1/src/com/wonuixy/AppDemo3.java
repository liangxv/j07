package com.wonuixy;

public class AppDemo3 {
    //小数的四则运算
    public static void main(String[] args) {
        double m = 3.5;
        double n = 4d; //加d代表是双精度类型

        System.out.println("m+n=" + (m + n));
        System.out.println("m-n=" + (m - n));
        System.out.println("m*n=" + (m * n));
        System.out.println("m/n=" + (m / n));
    }
}
